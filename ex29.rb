#Exerice 29

people = 20
cats = 3
dogs = 15

if people < cats 
  puts "Too many cats! The world is doomed!"
end

if people > cats
  puts "Not many cats! The world is saved!"
end

if people < dogs
  puts "The world is drooled on!"
end

if people > dogs
  puts "The world is dry"
end

dogs += 5

if people >= dogs
  puts "People are greater than or equal to dogs."
end

if people <= dogs
  puts "People are less than or equal to dogs."
end

if people == dogs 
  puts "People are dogs."
end

#Extra Credit

# if IF is true then you run the code under it. 
if people != dogs 
  puts "People are humans!"
end

